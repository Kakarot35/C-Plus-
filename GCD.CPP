#include <bits/stdc++.h>
using namespace std;

int main(){
    int n1,n2;
    cout << "What's your N1 = ";
    cin >> n1;
    cout << "What's your N2 = ";
    cin >> n2;
   
    int GCD;

    for(int i = 1; i <= min(n1,n2); i++){
        if( n1 % i == 0 && n2 % i == 0){
            GCD = i;
        }
    }
    cout << GCD;
}